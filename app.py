import streamlit as st
import numpy as np 
import tensorflow as tf
from sklearn.preprocessing import StandardScaler , OneHotEncoder , LabelEncoder
import pandas as pd
import pickle

# load the trained model
model = tf.keras.models.load_model('model.h5')

#load the encoder and scaler
with open('label_encoder_gender.pkl', 'rb') as file:
    label_encoder_gender = pickle.load(file)

with open('one_hot_encoder_geo.pkl', 'rb') as file:
    one_hot_encoder_geo = pickle.load(file)

with open('scaler.pkl', 'rb') as file:
    scaler = pickle.load(file)
    
## stramlit app

st.title("Customer Churn Prediction")
st.write("Enter the customer details to predict if they will churn or not.")

# user input
geography = st.selectbox("Geography", one_hot_encoder_geo.categories_[0])
gender = st.selectbox("Gender", label_encoder_gender.classes_)
age = st.slider("Age", min_value=18, max_value=100, value=30)
balance = st.number_input("Balance")
credit_score = st.number_input("Credit Score")
estimated_salary = st.number_input("Estimated Salary", min_value=0.0, value=50000.0)
tenure = st.slider("Tenure (Years)", min_value=0, max_value=10, value=1)
num_of_products = st.slider("Number of Products   ", min_value=1, max_value=4, value=1)
has_cr_card = st.selectbox("Has Credit Card", [0, 1])
is_active_member = st.selectbox("Is Active Member", [0, 1])

#prepare the input data
input_data = pd.DataFrame({
    'CreditScore': [credit_score],  
    'Gender': [label_encoder_gender.transform([gender])[0]],
    'Age': [age],
    'Tenure': [tenure],   
    'Balance': [balance],           
    'NumOfProducts': [num_of_products],   
    'HasCrCard': [has_cr_card],   
    'IsActiveMember': [is_active_member],
    'EstimatedSalary': [estimated_salary]  
})


# one-hot encode geography
geo_encoded = one_hot_encoder_geo.transform([[geography]]).toarray()
geo_encoded_df = pd.DataFrame(geo_encoded, columns=one_hot_encoder_geo.get_feature_names_out(['Geography']))

# concatenate the one-hot encoded geography columns to input_data
input_data = pd.concat([input_data.reset_index(drop=True), geo_encoded_df], axis=1)

#scale the input data
input_data_scaled = scaler.transform(input_data)

#predict the churn 
prediction = model.predict(input_data_scaled)
prediction_proba = prediction[0][0]

st.write(f'Churn Probability: {prediction_proba:.2f}')

if prediction_proba > 0.5:
    st.write('The customer is likely to churn.')
else:
    st.write('The customer is not likely to churn.')

